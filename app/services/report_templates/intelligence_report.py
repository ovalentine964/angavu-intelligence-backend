    def _generate_sections(self, product_code: str, data: Dict[str, Any]) -> str:
        """Generate HTML sections based on product type."""
        generators = {
            "soko_pulse": self._soko_pulse_sections,
            "biashara_pulse": self._biashara_pulse_sections,
            "alama_score": self._alama_score_sections,
            "jamii_insights": self._jamii_insights_sections,
            "tax_base_estimation": self._tax_base_sections,
            "distribution_gap": self._distribution_gap_sections,
        }
        generator = generators.get(product_code, self._generic_sections)
        return generator(data)

