    def _generic_sections(self, d: Dict) -> str:
        """Fallback generic sections."""
        metrics_html = ""
        for key, value in d.items():
            if isinstance(value, (int, float, str)) and key not in ("product", "version"):
                metrics_html += f'<div class="metric"><div class="value">{value}</div><div class="label">{key.replace("_", " ").title()}</div></div>'
        return f'<div class="section"><h2>📊 Key Metrics</h2>{metrics_html}</div>'

    def generate_summary_text(
        self,
        product_code: str,
        data: Dict[str, Any],
    ) -> str:
        """
        Generate a plain-text summary for WhatsApp/Telegram delivery.

        Args:
            product_code: Product identifier
            data: Intelligence product response data

        Returns:
            Plain text summary
        """
        generators = {
            "soko_pulse": self._soko_text_summary,
            "biashara_pulse": self._biashara_text_summary,
            "alama_score": self._alama_text_summary,
            "jamii_insights": self._jamii_text_summary,
            "tax_base_estimation": self._tax_text_summary,
            "distribution_gap": self._dist_gap_text_summary,
        }
        generator = generators.get(product_code, lambda d: "Report generated successfully.")
        return generator(data)

