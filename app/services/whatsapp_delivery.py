    # =========================================================================
    # Alerts
    # =========================================================================

    async def send_alert(
        self,
        worker_id: str,
        alert_type: str,
        alert_data: Optional[Dict] = None,
    ) -> bool:
        """
        Send an alert via WhatsApp.

        Alert types:
        - restock: Item below restock threshold
        - price_drop: Market price dropped for worker's products
        - credit_readiness: Worker qualifies for credit
        - unusual_activity: Unusual transaction pattern detected

        Args:
            worker_id: Worker UUID
            alert_type: Type of alert
            alert_data: Additional alert context

        Returns:
            True if sent successfully
        """
        user = await self._get_user(worker_id)
        if not user:
            return False

        phone = decrypt_value(user.phone_encrypted)
        lang = user.language or "sw"
        data = alert_data or {}

        try:
            message = self._format_alert(alert_type, data, lang)
            success = await self.bot.send_message(phone, message)

            if success:
                logger.info(
                    "alert_delivered",
                    worker_id=worker_id,
                    alert_type=alert_type,
                )
            return success

        except Exception as e:
            logger.error(
                "alert_delivery_failed",
                worker_id=worker_id,
                alert_type=alert_type,
                error=str(e),
            )
            return False

    def _format_alert(
        self, alert_type: str, data: Dict, language: str
    ) -> str:
        """Format an alert message for WhatsApp delivery."""

        if alert_type == "restock":
            item = data.get("item", "?")
            current = data.get("current_stock", 0)
            threshold = data.get("restock_threshold", 0)

            if language == "sw":
                return (
                    f"⚠️ *Taarifa ya Stock*\n\n"
                    f"📦 *{item}* imepungua!\n"
                    f"Stock ya sasa: {current}\n"
                    f"Kiwango cha chini: {threshold}\n\n"
                    f"🔔 *Nunua zaidi sasa ili usipitwe na wateja.*\n\n"
                    f"📱 Tumia Msaidizi App kurekodi ununuzi.\n"
                    f"🔗 https://msaidizi.app/download"
                )
            else:
                return (
                    f"⚠️ *Stock Alert*\n\n"
                    f"📦 *{item}* is running low!\n"
                    f"Current stock: {current}\n"
                    f"Minimum threshold: {threshold}\n\n"
                    f"🔔 *Restock now to avoid stockouts.*\n\n"
                    f"📱 Use Msaidizi App to record purchases.\n"
                    f"🔗 https://msaidizi.app/download"
                )

        elif alert_type == "price_drop":
            item = data.get("item", "?")
            old_price = data.get("old_price", 0)
            new_price = data.get("new_price", 0)
            drop_pct = data.get("drop_pct", 0)

            if language == "sw":
                return (
                    f"📉 *Bei Zimepungua*\n\n"
                    f"🏪 *{item}*:\n"
                    f"• Bei ya awali: KES {old_price:,.0f}\n"
                    f"• Bei ya sasa: KES {new_price:,.0f}\n"
                    f"• Kupungua: {drop_pct:.0f}%\n\n"
                    f"💡 *Fursa nzuri ya kununua stock!*\n\n"
                    f"📱 Tumia Msaidizi App.\n"
                    f"🔗 https://msaidizi.app/download"
                )
            else:
                return (
                    f"📉 *Price Drop Alert*\n\n"
                    f"🏪 *{item}*:\n"
                    f"• Previous price: KES {old_price:,.0f}\n"
                    f"• Current price: KES {new_price:,.0f}\n"
                    f"• Drop: {drop_pct:.0f}%\n\n"
                    f"💡 *Good opportunity to buy stock!*\n\n"
                    f"📱 Use Msaidizi App.\n"
                    f"🔗 https://msaidizi.app/download"
                )

        elif alert_type == "credit_readiness":
            score = data.get("alama_score", 0)
            band = data.get("band", "unknown")

            if language == "sw":
                return (
                    f"🏦 *Fursa ya Mkopo*\n\n"
                    f"Alama yako ya biashara: *{score}/100* ({band})\n\n"
                    f"💡 *Una sifa za kuomba mkopo!*\n"
                    f"Mkopo unaweza kukusaidia kuongeza stock "
                    f"na kupanua biashara yako.\n\n"
                    f"📱 Tumia Msaidizi App kuomba.\n"
                    f"🔗 https://msaidizi.app/download"
                )
            else:
                return (
                    f"🏦 *Credit Opportunity*\n\n"
                    f"Your business score: *{score}/100* ({band})\n\n"
                    f"💡 *You qualify for credit!*\n"
                    f"A loan can help you increase stock "
                    f"and grow your business.\n\n"
                    f"📱 Apply via Msaidizi App.\n"
                    f"🔗 https://msaidizi.app/download"
                )

        elif alert_type == "unusual_activity":
            item = data.get("item", "?")
            today_count = data.get("today_count", 0)
            avg_count = data.get("avg_count", 0)

            if language == "sw":
                return (
                    f"📊 *Mauzo ya Ajabu*\n\n"
                    f"🏪 *{item}* — mauzo yameongezeka sana leo!\n"
                    f"• Mauzo ya leo: {today_count}\n"
                    f"• Wastani wa kila siku: {avg_count:.0f}\n\n"
                    f"💡 *Angalia bei — mahitaji yamepanda!*\n\n"
                    f"📱 Tumia Msaidizi App.\n"
                    f"🔗 https://msaidizi.app/download"
                )
            else:
                return (
                    f"📊 *Unusual Activity*\n\n"
                    f"🏪 *{item}* — sales spike today!\n"
                    f"• Today's sales: {today_count}\n"
                    f"• Daily average: {avg_count:.0f}\n\n"
                    f"💡 *Check prices — demand has increased!*\n\n"
                    f"📱 Use Msaidizi App.\n"
                    f"🔗 https://msaidizi.app/download"
                )

        # Default alert
        if language == "sw":
            return f"🔔 *Taarifa*\n\n{data.get('message', 'Angalia biashara yako.')}"
        else:
            return f"🔔 *Alert*\n\n{data.get('message', 'Check your business.')}"

    # =========================================================================
    # Report Request Handling
    # =========================================================================

    async def handle_report_request(
        self, worker_id: str, request: str
    ) -> bool:
        """
        Handle a report request from a worker.

        Workers can request reports via WhatsApp by sending messages like:
        - "Ripoti ya leo" → daily report
        - "Ripoti ya wiki" → weekly report
        - "Ripoti ya mwezi" → monthly report

        Args:
            worker_id: Worker UUID
            request: The request message text

        Returns:
            True if report was sent successfully
        """
        user = await self._get_user(worker_id)
        if not user:
            return False

        phone = decrypt_value(user.phone_encrypted)

        try:
            response = await self.bot.process_message(
                phone=phone,
                message=request,
                message_type="text",
            )
            success = await self.bot.send_message(phone, response)

            if success:
                logger.info(
                    "report_request_handled",
                    worker_id=worker_id,
                    request=request[:50],
                )
            return success

        except Exception as e:
            logger.error(
                "report_request_failed",
                worker_id=worker_id,
                error=str(e),
            )
            return False

    # =========================================================================
    # Bulk Delivery (for scheduler)
    # =========================================================================

    async def send_daily_reports_to_all(self) -> Dict[str, int]:
        """
        Send daily reports to all active WhatsApp users.

        Called by the report scheduler at 7 PM EAT.

        Returns:
            Dict with total, sent, failed counts
        """
        return await self._bulk_send("daily")

    async def send_weekly_reports_to_all(self) -> Dict[str, int]:
        """
        Send weekly reports to all active WhatsApp users.

        Called by the report scheduler on Monday 8 AM EAT.

        Returns:
            Dict with total, sent, failed counts
        """
        return await self._bulk_send("weekly")

    async def send_monthly_reports_to_all(self) -> Dict[str, int]:
        """
        Send monthly reports to all active WhatsApp users.

        Called by the report scheduler on 1st of month 9 AM EAT.

        Returns:
            Dict with total, sent, failed counts
        """
        return await self._bulk_send("monthly")

    async def _bulk_send(self, report_type: str) -> Dict[str, int]:
        """Send a specific report type to all active WhatsApp users."""
        result = await self.db.execute(
            select(User).where(
                and_(
                    User.is_active == True,
                    User.channel == "whatsapp",
                )
            )
        )
        users = result.scalars().all()

        sent = 0
        failed = 0

        send_methods = {
            "daily": self.send_daily_report,
            "weekly": self.send_weekly_report,
            "monthly": self.send_monthly_report,
            "semiannual": self.send_semiannual_report,
            "annual": self.send_annual_report,
        }

        send_fn = send_methods.get(report_type, self.send_daily_report)

        for user in users:
            try:
                success = await send_fn(str(user.id))
                if success:
                    sent += 1
                else:
                    failed += 1
            except Exception as e:
                logger.error(
                    "bulk_send_user_failed",
                    user_id=str(user.id),
                    report_type=report_type,
                    error=str(e),
                )
                failed += 1

        logger.info(
            "bulk_send_completed",
            report_type=report_type,
            total=len(users),
            sent=sent,
            failed=failed,
        )

        return {
            "total": len(users),
            "sent": sent,
            "failed": failed,
        }

    # =========================================================================
    # Chart Generation
    # =========================================================================

    async def _generate_weekly_chart(
        self, report, language: str
    ) -> Optional[bytes]:
        """
        Generate a chart image for the weekly report.

        Returns PNG image bytes, or None if chart generation fails.
        """
        try:
            from app.services.whatsapp_charts import WhatsAppCharts
            charts = WhatsAppCharts()
            return charts.generate_weekly_sales_chart(report, language)
        except ImportError:
            logger.warning("whatsapp_charts_not_available")
            return None
        except Exception as e:
            logger.warning("chart_generation_failed", error=str(e))
            return None

    # =========================================================================
    # Helpers
    # =========================================================================

    async def _get_user(self, worker_id: str) -> Optional[User]:
        """Get user by ID."""
        result = await self.db.execute(
            select(User).where(
                and_(User.id == worker_id, User.is_active == True)
            )
        )
        return result.scalar_one_or_none()
