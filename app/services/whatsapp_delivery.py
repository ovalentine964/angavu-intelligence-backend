"""
WhatsApp Delivery Service — Biashara Intelligence Report Channel.

WhatsApp is a PURE REPORT DELIVERY CHANNEL. Workers receive reports when online.

Reports are generated by Biashara Intelligence cloud model and delivered
via WhatsApp. The Msaidizi app handles transaction recording (offline).

NOT for:
- Transaction recording (Msaidizi app, offline)
- Deep analysis requests (Msaidizi app → Biashara Intelligence cloud API)
- Interactive commands (Msaidizi app)

ONLY for:
- Scheduled reports (daily, weekly, monthly, 6-month, yearly)
- Alerts (restock, price drop, credit readiness, unusual activity)
- Report requests ("Ripoti ya leo")

Report Schedule:
- Daily: 7 PM EAT (evening briefing)
- Weekly: Monday 8 AM EAT (weekly summary)
- Monthly: 1st of month 9 AM EAT (monthly review)
- Semi-annual: June 30 & December 31 (semi-annual review)
- Annual: December 31 (annual review)
"""

